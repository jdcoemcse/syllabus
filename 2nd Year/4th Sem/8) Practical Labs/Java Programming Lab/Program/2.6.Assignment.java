class Assignment {
    public static void main(String[] args) 
    {
                System.out.println("\nAssignment Operator\n");
                
                int a = 7; 
                
                System.out.println( "Value of a = " + a); 
                
                System.out.println("a += 3: " + (a += 3)); 
                System.out.println("a -= 2: " + (a -= 2)); 
                System.out.println("a *= 4: " + (a *= 4)); 
                System.out.println("a /= 3: " + (a /= 3)); 
                System.out.println("a %= 2: " + (a %= 2)); 
    }
}
